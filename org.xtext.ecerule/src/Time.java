public class Time {
	String allenOp;
	int time;

	public Time(String allenOp, int time) {
		super();
		this.allenOp = allenOp;
		this.time = time;
	}

	public String getAllenOp() {
		return allenOp;
	}

	public void setAllenOp(String allenOp) {
		this.allenOp = allenOp;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

}
